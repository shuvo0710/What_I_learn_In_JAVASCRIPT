//creating a function
function square(num) {
  var result = num * num;
  return result;
}
//calling function:
console.log("square of 5 = " + square(5));

// add function

function addition(a, b) {
  const sum = a + b;
  return sum;
}
console.log("sum of 5 and 6 = " + addition(5, 6));

// substraction

function substraction(a, b) {
  const sub = a - b;
  return sub;
}
console.log("10 and 5 sub is = " + substraction(10, 5));
